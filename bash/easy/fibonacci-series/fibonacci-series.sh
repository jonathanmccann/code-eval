#!/bin/bash

while read -r LINE || [[ -n $LINE ]]; do
	PREVIOUS_NUMBER=1
	CURRENT_NUMBER=1
	FIBONACCI_NUMBER=0

	for ((i = 0; i < $LINE; i++)); do
		PREVIOUS_NUMBER=$CURRENT_NUMBER
		CURRENT_NUMBER=$FIBONACCI_NUMBER

		((FIBONACCI_NUMBER=$PREVIOUS_NUMBER+$CURRENT_NUMBER))
	done

	echo $FIBONACCI_NUMBER
done < $1